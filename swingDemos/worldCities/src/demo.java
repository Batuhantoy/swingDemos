    public void populateTable(){        
        model = (DefaultTableModel)tblCities.getModel();  
        model.setRowCount(0);
        try {
            ArrayList<City> cities = getCities();
            for(City city:cities){
                Object[] row ={city.getId(),city.getName(),
                    city.getCountryCode(),city.getDistrict(),city.getPopulation()};
                model.addRow(row);
            }
        } catch (SQLException ex) {
            
        }
    }
    public ArrayList<City> getCities()throws SQLException{
        Connection con =null;
        DbHelper dbHelper=new DbHelper();
        Statement statement = null;
        ResultSet result;
        ArrayList<City> cities=null;//burda da newleyebiliriz ama veritabanına baglanmazsa 
        //boşuna bellekte yer tutmasın diye aşagıda newliyoz temiz kod yazma olayı yani
        try{
            con=dbHelper.getConnection();
            statement=con.createStatement();
            result=statement.executeQuery("select * from city");  
            cities = new ArrayList<City>();
            while(result.next()){
                cities.add(new City(
                        result.getInt("ID"),
                        result.getString("Name"),
                        result.getString("CountryCode"),
                        result.getString("District"),
                        result.getInt("Population")            
                ));
            }
        }catch(SQLException e){
            dbHelper.showErrorMessage(e);
        }finally{
            statement.close();
            con.close();
        }
        return cities;
    }
    
